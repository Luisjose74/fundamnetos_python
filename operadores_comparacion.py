#operadores de comparacion
print(5 == 5) # si es igual a 5, devuelve True
print(5 != 3) # si no es igual a 3, devuelve True
print(5 > 3) # si es mayor que 3, devuelve True
print(5 < 10) # si es menor que 10, devuelve True
print(5 >= 5) # si es mayor o igual a 5, devuelve True
print(5 <= 10) # si es menor o igual a 10, devuelve True


print(8 == 5) # si es igual a 5, devuelve False
print(3 != 3) # si no es igual a 3, devuelve False
